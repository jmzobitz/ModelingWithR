(Inspired by @logan_mathematical_2009) A population of fish used as food is an example of a renewable resource. This population decreases due to harvesting the fish.  As long as the rate of harvest is smaller than the replacement rate, theoretically the population would be considered a renewable resource.  However fish do have natural predators, which may also be harvested at the same time.  A model that describes this interaction is

\begin{equation}
\begin{split}
\frac{dN}{dt} &= rN - cNP - \rho E N \\
\frac{dP}{dt} &= bcNP - mP - \sigma EP,
\end{split}
\end{equation}

where $E$ is the fishing effort and $\rho$ and $\sigma$ are the catchability coefficients for the prey $N$ and predator $P$.


a. Determine all the equilibrium solutions for this model.
b. Construct the Jacobian matrix for each of the the equilibrium solutions.
c. Select different parameter values.  With the Jacobian, construct a phase plane diagram for each of the equilibrium solutions.
d. In the absence of fishing $E=0$.  How do these equilibrium solutions compare to the ones from the previous part?
  
